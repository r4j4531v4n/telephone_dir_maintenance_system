    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice;
        Employee obj = new Employee();
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee name cant be empty", "Exception", JOptionPane.INFORMATION_MESSAGE);
        } else {
            obj.setEmpName(jTextField1.getText());
            obj.setDeptCode(Integer.parseInt((String) jComboBox2.getSelectedItem()));
            obj.setLocation((String) jComboBox1.getSelectedItem());
            boolean status = obj.addToDB();
            jTextField2.setText(Integer.toString(obj.getEmpId()));
            if (status) {
                choice = JOptionPane.showConfirmDialog(null, "Add another employee?", "Intermediate Screen", JOptionPane.YES_NO_OPTION);
                if (choice == 0) {
                    jTextField1.setText(null);
                    jTextField2.setText(null);

                } else {
                    dispose();
                    EmployeeMenu obj2 = new EmployeeMenu();
                    obj2.setLocation(300,50);
                    //obj2.setLocationRelativeTo(null);
                    obj2.setVisible(true);
                }
            }
            jTextField1.setText(null);
            jTextField2.setText(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

