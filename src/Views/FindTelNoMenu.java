    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice;
        Telephone obj = new Telephone();
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee name cant be empty", "Error", JOptionPane.INFORMATION_MESSAGE);

        } else {
            
            data = obj.searchDB(jTextField1.getText());
            jTable1 = new javax.swing.JTable();
            jTable1.setModel(new javax.swing.table.DefaultTableModel(data, header));
            jScrollPane1.setViewportView(jTable1);
            jTable1.setVisible(true);
            if (!data.isEmpty()) {
                choice = JOptionPane.showConfirmDialog(null, "Find another tel no?", "Intermediate Screen", JOptionPane.YES_NO_OPTION);
                if (choice == 0) {
                    jTextField1.setText(null);
                    jTable1.setVisible(false);
                } else {
                    dispose();
                    TelephoneMenu obj2 = new TelephoneMenu();
                    obj2.setLocation(300,50);
                    //obj2.setLocationRelativeTo(null);
                    obj2.setVisible(true);
                }
            } else {
                jTextField1.setText(null);
                JOptionPane.showMessageDialog(null, "Employee name doesnt exist", "Error", JOptionPane.INFORMATION_MESSAGE);

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

