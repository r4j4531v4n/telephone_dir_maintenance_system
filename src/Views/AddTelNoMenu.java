    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice;
        Telephone obj1 = new Telephone();
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee id cant be empty", "Error", JOptionPane.INFORMATION_MESSAGE);

        } else {
            obj1.setEmpId(Integer.parseInt(jTextField1.getText()));
            boolean status = obj1.addToDB();
            jTextField2.setText(String.valueOf(obj1.getTelNo()));
            jTextField3.setText(String.valueOf(obj1.getDeptCode()));
            jTextField4.setText(obj1.getLocation());
            if (status) {
                choice = JOptionPane.showConfirmDialog(null, "Add another telephone no?", "Intermediate Screen", JOptionPane.YES_NO_OPTION);
                if (choice == 0) {
                    jTextField1.setText(null);
                    jTextField2.setText(null);
                    jTextField3.setText(null);
                    jTextField4.setText(null);
                } else {
                    dispose();
                    TelephoneMenu obj = new TelephoneMenu();
                    obj.setLocation(300,50);
                    //obj.setLocationRelativeTo(null);
                    obj.setVisible(true);
                }
            }
            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

