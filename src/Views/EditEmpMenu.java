    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Employee obj1 = new Employee();
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Employee id cant be empty", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            obj1.setEmpId(Integer.parseInt(jTextField1.getText()));
            boolean status=obj1.findEmp();
            if (status){
            jComboBox2.setSelectedItem(String.valueOf(obj1.getDeptCode()));
            jTextField3.setText(obj1.getEmpName());
            jComboBox1.setSelectedItem(obj1.getLocation());
            }
            else{
                jTextField1.setText(null);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int choice;
        Employee obj2 = new Employee();
        obj2.setEmpId(Integer.parseInt(jTextField1.getText()));
        obj2.setEmpName(jTextField3.getText());
        obj2.setDeptCode(Integer.parseInt((String) jComboBox2.getSelectedItem()));
        obj2.setLocation((String) jComboBox1.getSelectedItem());
        boolean status = obj2.updateDB();
        if (status) {
            choice = JOptionPane.showConfirmDialog(null, "Edit another employee?", "Intermediate Screen", JOptionPane.YES_NO_OPTION);
            if (choice == 0) {
                jTextField1.setText(null);
                jTextField3.setText(null);
                jTextField3.setEditable(false);
            } else {
                dispose();
                EmployeeMenu obj3 = new EmployeeMenu();
                obj3.setLocation(300,50);
                //obj3.setLocationRelativeTo(null);
                obj3.setVisible(true);
            }
        }
        jTextField1.setText(null);
        jTextField3.setText(null);
    }//GEN-LAST:event_jButton4ActionPerformed

